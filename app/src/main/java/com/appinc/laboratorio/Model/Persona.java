package com.appinc.laboratorio.Model;

public class Persona {

    private int Id;
    private String Nombre;

    public Persona() {
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }
}